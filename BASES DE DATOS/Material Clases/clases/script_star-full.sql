with row_generator as (
SELECT to_date('01012000','ddmmyyyy') + LEVEL - 1 DT
FROM sys.dual
CONNECT BY LEVEL <=
to_number(to_date('31122017','ddmmyyyy') -
to_date('01012015','ddmmyyyy')) +1 )
SELECT DISTINCT extract(year from DT) as anio, extract(month from DT) as mes, extract(day from DT) as dia
from row_generator
order by 1,2,3;

