CREATE TABLE cliente
   (
   id_cliente NUMBER(10,0) GENERATED BY DEFAULT ON NULL AS IDENTITY ,
   codigo_cliente number(10,0) not null ,
   nombre varchar2(100) not null,
   estadoCivil varchar2(10) default 'SOLTERO' not null,
   edad number(3,0) default 0 not null,
  CONSTRAINT pk_cliente PRIMARY KEY (id_cliente)
 
   );

CREATE TABLE tiempo
   (
   id_tiempo NUMBER(10,0) GENERATED BY DEFAULT ON NULL AS IDENTITY ,
   anio int,
   mes int,
   dia int,
   CONSTRAINT pk_tiempo PRIMARY KEY (id_tiempo)
   );


 
CREATE TABLE ventas
   (    
   id_tiempo NUMBER(10,0) GENERATED BY DEFAULT ON NULL AS IDENTITY ,
   id_cliente numeric(10,0) not null,
   Ingreso numeric,
   costo numeric,
     CONSTRAINT pk_ventas PRIMARY KEY (id_tiempo, id_cliente)  ,
   foreign key (id_tiempo) references tiempo,
   foreign key (id_cliente) references cliente
   );

